// Searches a string for a match against a regular expression
// and returns the matches
class Matches
{
 public static void main(String[]R)
 {
  String str = "hello";
  String str1 = "hello";
  String str2 = "Hello";
  System.out.println(str.matches(str1));
  System.out.println(str.matches(str2));
 }
}